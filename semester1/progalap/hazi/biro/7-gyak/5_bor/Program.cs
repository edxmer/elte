using System;


//namespace _5_bor;


struct Evek {
    public int db, ar;
}


class Program
{
    static void Main(string[] args)
    {
        // 1-es indexelést fogok használni.

        // Deklaráció
        int n;
        Evek[] ev = new Evek[101];

        int termelesminind;
        bool van1000; int max1000;
        int kulonbozoarak;
        int tobbdb; int[] tobb = new int[101];

        int minert;
        int feltmaxind;
        int i, j;
        int tobbmax;

        string[]? line;
        bool success;

        // Beolvasás

        do {
            success = int.TryParse(Console.ReadLine(), out n);

            success = success && (1 <= n && n <= 100);

            if (!success)
                Console.Error.WriteLine("Sikertelen beolvasás.");
        } while (!success);
        

        for (i = 1; i <= n; ++i) {
            
            do {
                Console.Error.Write($"{i}: ");
                line = Console.ReadLine().Split(' ');
                if (line != null) {
                    success = int.TryParse(line[0], out ev[i].db);
                    success = success && int.TryParse(line[1], out ev[i].ar);

                    success = success && (0 <= ev[i].db && 0 <= ev[i].ar);
                }
                else 
                    success=false;

                if (!success)
                    Console.Error.WriteLine("Sikertelen beolvasás.");
            } while (!success);
        }


        // Feoldolgozás


        // 1.
        termelesminind = 1;
        minert = ev[1].db;

        for (i = 2; i <= n; ++i) {
            if (ev[i].db < minert) {
                minert = ev[i].db;
                termelesminind = i;
            }
        }

        // 2.
        van1000 = false;
        max1000 = -1; // ha ez nincs itt akkor sír a compiler
        for (i = 1; i <= n; ++i) {
            if (!(1000<ev[i].db)) {}
            else if (van1000 && (1000<ev[i].db)) {
                if (max1000<ev[i].ar) {
                    max1000 = ev[i].ar;
                    feltmaxind = i;
                }
            }
            else if (!van1000 && (1000<ev[i].db)) {
                van1000 = true;
                max1000 = ev[i].ar;
                feltmaxind = i;
            }
        }

        // 3.
        kulonbozoarak = 0;
        for (i = 1; i <= n; ++i) {
            j = 1;
            while (!(ev[j].ar == ev[i].ar))
                j++;
            
            if (i==j)
                kulonbozoarak++;
        }

        // 4.
        tobbdb = 0;
        for (i = 1; i <= n; ++i) {
            tobbmax = ev[1].db;
            for (j = 1; j <= i-1; ++j)
                if (tobbmax < ev[j].db)
                    tobbmax = ev[j].db;

            if (tobbmax < ev[i].db) {
                tobbdb++;
                tobb[tobbdb] = i;
            }
        }

        // Kiírás
        Console.Error.Write("\nMegoldások:\n");

        Console.WriteLine(termelesminind);
        
        if (van1000)
            Console.WriteLine(max1000);
        else
            Console.WriteLine(-1);
        
        Console.WriteLine(kulonbozoarak);

        Console.Write(tobbdb);
        for (i = 1; i <= tobbdb; ++i)
            Console.Write($" {tobb[i]}");
        Console.WriteLine();

    }
}
