using System;



struct Varos {
    public int tav, ar;
}

//namespace _4_utazas;
class Program
{
    static void Main(string[] args)
    {
        // A tömbök 1-es indexelésűek lesznek

        // Deklaráció
        int n;
        Varos[] varosok;
        
        int legtavolabbiind, maxert;
        bool van1000; int legdragabb1000;
        int egyertelmudb, db2;
        int km100ftdb; int[] km100ftinds;


        // Beolvasás
        bool jo;
        do {
            Console.Error.Write("n: ");
            jo = int.TryParse(Console.ReadLine(), out n);
            if (jo && !(1 <= n && n <= 100))
                jo = false;
            if (!jo)
                Console.Error.WriteLine("Helytelen bemenet!");
        } while (!jo);
        
        varosok = new Varos[n+1];
        km100ftinds = new int[n+1];

        for (int i = 1; i <= n; i++) {
            do {
                Console.Error.Write($"{i}.: ");
                string[] line = Console.ReadLine().Split(" ");
                jo = int.TryParse(line[0], out varosok[i].tav);
                jo = jo && int.TryParse(line[1], out varosok[i].ar);
                if (jo && !(0<=varosok[i].tav && 0<=varosok[i].ar))
                    jo = false;
                if (!jo)
                    Console.Error.WriteLine("Helytelen bemenet!");
            } while (!jo);
        }

        // Feldolgozás

        // A.
        legtavolabbiind = 1;
        maxert = varosok[1].tav;
        
        for(int i = 2; i <= n; i++) {
            if (varosok[i].tav>maxert) {
                legtavolabbiind = i;
                maxert = varosok[i].tav;
            }
        }

        // B.
        legdragabb1000 = -1; // hogy ne legyen error
        van1000 = false;
        for(int i = 1; i <= n; i++) {
            if (!(varosok[i].tav < 1000)) ;
            else if (van1000) {
                if (varosok[i].ar > legdragabb1000)
                    legdragabb1000 = varosok[i].ar;
            }
            else {
                van1000 = true;
                legdragabb1000 = varosok[i].ar;
            }
        }

        // C.
        egyertelmudb = 0;
        for (int i = 1; i <= n; i++) {
            db2 = 0;
            for (int j = 1; j <= n; j++)
                if (varosok[i].ar == varosok[j].ar)
                    db2++;
            if (db2 == 1)
                egyertelmudb++;
        }

        // D.
        km100ftdb = 0;
        for (int i = 1; i<= n; i++) {
            if (((float)varosok[i].ar)/((float)varosok[i].tav)>100.0) {
                km100ftdb++;
                km100ftinds[km100ftdb]=i;
            }
        }


        // Kiírás
        Console.Error.WriteLine();
        Console.Error.WriteLine("(A.)");
        Console.Error.Write("A legtávolabbi város sorszáma: ");
        Console.WriteLine(legtavolabbiind);
        Console.Error.WriteLine();
        

        Console.Error.WriteLine("(B.)");
        if (van1000) {
            Console.Error.Write("<1000km legdrágább jegy: ");
            Console.Write(legdragabb1000);
            Console.Error.Write(" HUF");
            Console.WriteLine();
        }
        else {
            Console.Error.Write("Nincs <1000km-re menő repülő jegy. (");
            Console.Write(-1);
            Console.Error.Write(")");
            Console.WriteLine();
        }
        Console.Error.WriteLine();


        Console.Error.WriteLine("(C.)");
        Console.Write(egyertelmudb);
        Console.Error.Write(" db jegynek az ára egyedi.");
        Console.WriteLine();
        Console.Error.WriteLine();


        Console.Error.WriteLine("(D.)");
        Console.Error.Write("HUF/km > 100: ");
        Console.Write(km100ftdb);
        Console.Error.WriteLine(" db:");
        for (int i = 1; i <= km100ftdb; i++) {
            Console.Error.Write($"\t{i}.:");
            Console.Write($" {km100ftinds[i]}");
            Console.Error.WriteLine();
        }
        Console.WriteLine();

    }
}
